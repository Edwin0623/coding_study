package Jul13;

// String is defined as array of characters.

public class toLowerCase {
    public static void main(String[] args) {
        // toLowerCase - converts all characters of the string into lowercase letter.
        // toUpperCase - converts all characters of the string into uppercase letter.

        String model = "iPHONE6";
        System.out.println(model.toLowerCase());
        System.out.println(model.toUpperCase());
    }
}
